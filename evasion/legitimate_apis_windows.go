func decDLL(enc []byte) *syscall.LazyDLL {
	return syscall.NewLazyDLL(xorDec(enc, xk))
}

func decProc(dll *syscall.LazyDLL, enc []byte) *syscall.LazyProc {
	return dll.NewProc(xorDec(enc, xk))
}

