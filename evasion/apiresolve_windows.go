// 动态密钥派生函数
func deriveKey(baseKey byte, offset int) byte {
	return (baseKey + byte(offset)*0x13) ^ 0x55
}

// 基础解密 — 简单XOR
func xorDec(data []byte, key byte) string {
	out := make([]byte, len(data))
	for i, b := range data {
		out[i] = b ^ key
	}
	return string(out)
}

// 多层解密：轮询XOR + 位旋转
func multiDec(data []byte, baseKey byte) string {
	out := make([]byte, len(data))
	for i, b := range data {
		key := deriveKey(baseKey, i)
		out[i] = ((b ^ key) << 2) | ((b ^ key) >> 6)
	}
	return string(out)
}

const xk byte = 0x7F

// XorDecPub 公开的XOR解密函数，供其他包使用
func XorDecPub(data []byte) string {
	return xorDec(data, xk)
}

